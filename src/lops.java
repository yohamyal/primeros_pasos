// while y for

public class lops {
    public static void main (String[] args) {
// los lops o siglos en este caso while(mientras que...)
// es utiliado para que con una condicion se vayan ajecutando,
// hasta que llegue al resultado deseado
        int contador = 1;
// mientras contador no sea menor o igual que 10, no dejar de ejecutar
        while (contador <= 10) {
            System.out.println(contador);
            //contador = (contador + 1);
            contador++;
           // contador = +1;
        }

    }
}
